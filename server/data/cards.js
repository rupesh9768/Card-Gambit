export const cards = cardsBySpecies.flatMap((set) =>
  set.cards.map((card) => {
    const cardWithStats = {
      ...card,
      species: set.species,
      race: set.race,
    };

    return {
      id: card.id,
      name: card.name,
      species: set.species,
      race: set.race,
      health: card.health,
      attack: card.attack,
      defense: card.defense,
      rating: calculateRating(cardWithStats),
      ability: card.ability,
      rarity: card.rarity,
      collected: card.collected,
      quantity: card.collected ? 1 : 0,
      imageUrl: card.imageUrl ?? '',
    };
  }),
);

